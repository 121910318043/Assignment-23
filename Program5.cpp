#include<iostream>
using namespace std;
//Write a C++ program to calculate the volume of a cuboid.
int main()
{
    int l,b,h;
    cout<<"Enter Length of the Cuboid ";
    cin>>l;
    cout<<"Enter Breadth of the Cuboid ";
    cin>>b;
    cout<<"Enter Height of the Cuboid ";
    cin>>h;
    cout<<"The Volume of the Cuboid = "<<l*b*h;
    cout<<endl;
    return 0;
}
